export type NotificationTypeKey =
  | 'BOOKING_REQUESTED'
  | 'BOOKING_ACCEPTED'
  | 'BOOKING_DECLINED'
  | 'PAYMENT_SUCCEEDED'
  | 'PAYMENT_FAILED'
  | 'BOOKING_CANCELLED'
  | 'BOOKING_COMPLETED'
  | 'NEW_MESSAGE'

export function notificationText(
  lang: Lang,
  type: NotificationTypeKey,
  listingTitle: string,
): { title: string; body: string } {
  const titleKey = `notifTitle${type}` as TranslationKey
  const bodyKey = `notifBody${type}` as TranslationKey
  const title = translations[lang][titleKey] ?? type
  const bodyTemplate = translations[lang][bodyKey] ?? ''
  return { title, body: bodyTemplate.replace('{listing}', listingTitle) }
}
