def esc(s: str) -> str:
    """Escape a string for a TypeScript single-quoted literal (ASCII-safe)."""
    out = []
    for ch in s:
        o = ord(ch)
        if ch == "\\":
            out.append("\\\\")
        elif ch == "'":
            out.append("\\'")
        elif ch == "\n":
            out.append("\\n")
        elif o < 128:
            out.append(ch)
        else:
            out.append(f"\\u{o:04X}")
    return "".join(out)


def block(d: dict[str, str], indent: int = 4) -> str:
    pad = " " * indent
    lines = []
    for k, v in d.items():
        if "\n" in v:
            parts = v.split("\n")
            joined = " +\n".join(f"{pad}  '{esc(p)}'" for p in parts)
            lines.append(f"{pad}{k}:\n{joined},")
        else:
            lines.append(f"{pad}{k}: '{esc(v)}',")
    return "\n".join(lines)


